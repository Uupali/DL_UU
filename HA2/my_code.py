# Exercise 4: Residual Connections
# =============================================================================
class ResidualBlock(nn.Module):
    """Basic residual block with two 3x3 convs + skip connection."""
    def __init__(self, in_ch, out_ch, stride=1):
        super().__init__()
        self.conv1 = nn.Conv2d(in_ch, out_ch, 3, stride=stride, padding=1, bias=False)
        self.conv2 = nn.Conv2d(out_ch, out_ch, 3, stride=1, padding=1, bias=False)
        self.skip = nn.Sequential()
        if stride != 1 or in_ch != out_ch:
            self.skip = nn.Conv2d(in_ch, out_ch, 1, stride=stride, bias=False)

    def forward(self, x):
        out = F.relu(self.conv1(x))
        out = self.conv2(out)
        out += self.skip(x)
        return F.relu(out)

# Plain deeper (no skip)
class PlainDeepCNN(nn.Module):
    def __init__(self):
        super().__init__()
        self.init = nn.Conv2d(1, 8, 3, 1, 1)
        self.l1 = nn.Conv2d(8, 8, 3, 1, 1)
        self.l2 = nn.Conv2d(8, 16, 3, 2, 1)
        self.l2e = nn.Conv2d(16, 16, 3, 1, 1)
        self.l3 = nn.Conv2d(16, 32, 3, 2, 1)
        self.l3e = nn.Conv2d(32, 32, 3, 1, 1)
        self.fc = nn.Linear(32*7*7, 10)

    def forward(self, x):
        x = F.relu(self.init(x))
        x = F.relu(self.l1(x))
        x = F.relu(self.l2(x))
        x = F.relu(self.l2e(x))
        x = F.relu(self.l3(x))
        x = F.relu(self.l3e(x))
        x = x.view(x.size(0), -1)
        return self.fc(x)

# Shallow residual
class ShallowResidualCNN(nn.Module):
    def __init__(self):
        super().__init__()
        self.init = nn.Conv2d(1, 8, 3, 1, 1)
        self.l1 = ResidualBlock(8, 8, 1)
        self.l2 = ResidualBlock(8, 16, 2)
        self.l3 = ResidualBlock(16, 32, 2)
        self.fc = nn.Linear(32*7*7, 10)

    def forward(self, x):
        x = F.relu(self.init(x))
        x = self.l1(x)
        x = self.l2(x)
        x = self.l3(x)
        x = x.view(x.size(0), -1)
        return self.fc(x)

# Deeper residual
class DeeperResidualCNN(nn.Module):
    def __init__(self):
        super().__init__()
        self.init = nn.Conv2d(1, 8, 3, 1, 1)
        self.l1_1 = ResidualBlock(8, 8, 1)
        self.l1_2 = ResidualBlock(8, 8, 1)
        self.l2_1 = ResidualBlock(8, 16, 2)
        self.l2_2 = ResidualBlock(16, 16, 1)
        self.l3_1 = ResidualBlock(16, 32, 2)
        self.l3_2 = ResidualBlock(32, 32, 1)
        self.fc = nn.Linear(32*7*7, 10)

    def forward(self, x):
        x = F.relu(self.init(x))
        x = self.l1_1(x)
        x = self.l1_2(x)
        x = self.l2_1(x)
        x = self.l2_2(x)
        x = self.l3_1(x)
        x = self.l3_2(x)
        x = x.view(x.size(0), -1)
        return self.fc(x)

