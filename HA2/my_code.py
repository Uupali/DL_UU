# Exercise 5: Regularization (BN & Dropout)
# =============================================================================
class RegularizedResidualCNN(nn.Module):
    """Residual CNN with optional BN or Dropout."""
    def __init__(self, use_bn=False, dropout_p=0.0):
        super().__init__()
        self.init = nn.Conv2d(1, 8, 3, 1, 1, bias=not use_bn)
        self.bn_init = nn.BatchNorm2d(8) if use_bn else nn.Identity()

        self.l1 = ResidualBlock(8, 8, 1, use_bn, dropout_p)
        self.l2 = ResidualBlock(8, 16, 2, use_bn, dropout_p)
        self.l3 = ResidualBlock(16, 32, 2, use_bn, dropout_p)

        self.dropout_fc = nn.Dropout(dropout_p) if dropout_p > 0 else nn.Identity()
        self.fc = nn.Linear(32*7*7, 10)

    def forward(self, x):
        x = self.init(x)
        x = self.bn_init(x)
        x = F.relu(x)
        x = self.l1(x)
        x = self.l2(x)
        x = self.l3(x)
        x = x.view(x.size(0), -1)
        x = self.dropout_fc(x)
        return self.fc(x)

