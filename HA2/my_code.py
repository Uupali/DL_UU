# Reusable training function (used for all exercises)
# =============================================================================
def train_model(model, optimizer, train_loader, test_loader, title, epochs=50):
    """Train model and return lists for plotting + elapsed time."""
    train_costs, test_costs = [], []
    train_accs, test_accs = [], []

    start = time.time()

    for epoch in range(epochs):
        model.train()
        batch_losses, batch_accs = [], []

        for x, y in train_loader:
            x, y = x.to(device), y.to(device)
            optimizer.zero_grad()
            out = model(x)
            loss = criterion(out, y)
            loss.backward()
            optimizer.step()

            batch_losses.append(loss.item())
            pred = out.argmax(dim=1)
            acc = (pred == y).float().mean().item()
            batch_accs.append(acc)

        train_costs.append(np.mean(batch_losses))
        train_accs.append(np.mean(batch_accs))

        model.eval()
        test_loss_sum, test_correct, n = 0.0, 0, 0
        with torch.no_grad():
            for x, y in test_loader:
                x, y = x.to(device), y.to(device)
                out = model(x)
                loss = criterion(out, y)
                test_loss_sum += loss.item() * x.size(0)
                pred = out.argmax(dim=1)
                test_correct += (pred == y).sum().item()
                n += y.size(0)

        test_costs.append(test_loss_sum / n)
        test_accs.append(test_correct / n)

        print(f"Epoch {epoch+1}/{epochs}: Train {train_costs[-1]:.4f} / {train_accs[-1]:.4f} | "
              f"Test {test_costs[-1]:.4f} / {test_accs[-1]:.4f}")

    elapsed = time.time() - start
    print(f"{title} completed in {elapsed:.1f} sec")

    training_curve_plot(
        title=title,
        train_costs=train_costs,
        test_costs=test_costs,
        train_accuracy=train_accs,
        test_accuracy=test_accs,
        batch_size=batch_size,
        learning_rate=optimizer.param_groups[0]['lr'],
        num_epochs=epochs,
        elapsed=elapsed
    )

    return train_costs, test_costs, train_accs, test_accs, elapsed

